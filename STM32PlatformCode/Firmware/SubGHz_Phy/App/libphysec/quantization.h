// the LSB of quant_type tells
// if the quantization is lossy or not
// The second LSB of quant_type tells
// if the quantization is blockwise or not
#define QUANT_LOSSY_MASK (1)
#define QUANT_BLOCKWISE_MASK (2) // 1<<1
#define QUANT_IS_LOSSY(quant_type) (quant_type & (QUANT_LOSSY_MASK))
#define QUANT_IS_BLOCKWISE(quant_type) (quant_type & (QUANT_BLOCKWISE_MASK))
#define QUANT_IS_EXCURSION(quant_type)                                         \
  (((quant_type) == QUANT_SB_EXCURSION_LOSSY ||                                \
    (quant_type) == QUANT_MB_EXCURSION_LOSSY))

