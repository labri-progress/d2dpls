/*!
 *	Dynamic Quantization Diversity Block information
 */
typedef struct {
  // ID for diversity block start (first index in measurement set)
  uint32_t start;
  // ID for diversity block end (last index in measurement set)
  uint32_t end;
  // number of bits quantized per sample in the diversity block
  uint8_t nbits_per_sample;
} dynquant_diversity_block_t;

/*!
 *	Quantization parameters
 */
// Allow knowing how many bits where generated by sample, boundaries of the
// sequences when the number of bits to quantize is dynamically decided
typedef struct __attribute__((__packed__)) {
  bool dynamic;
  union {
    uint8_t nbits_per_sample;
    dynquant_diversity_block_t *diversity_blocks_info;
  };
} quant_lossy_params_t;

typedef struct __attribute__((__packed__)) {
  uint8_t value;
  quant_index_t index;
} quant_excursion_index_t;

